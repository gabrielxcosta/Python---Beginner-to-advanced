'''
:s - Texto (strings)
:d - Inteiros (int)
:.(Número)f - Qtd de casas decimais (float)
:(Caractere)(> ou < ou ^)(Qtd)(Tipo - s, d ou f)

'''

# Podemos formatar valores usando f'string e '{}'.format(variável/valor/dado)
print('A divisão de 10 por 3 é {}'.format(10 / 3))

print('A divisão de 10 por 3 com 2 casas decimais é {:.2f}'.format(10 / 3))